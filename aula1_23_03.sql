
create database produtos_limpeza;

use produtos_limpeza;

create table Status(
	id int primary key auto_increment,
    status varchar(10)
);

create table Cliente(
	id int primary key auto_increment,
    nome varchar(30),
    endereco varchar(100),
    telefone char(15),
    filiacao varchar(100),
    limite_credito float,
    id_status int,
    foreign key (id_status) references Status(id)
);

create table Categoria(
	id int primary key auto_increment,
    categoria varchar(30)
);

create table Fornecedor(
	id int primary key auto_increment,
    cnpj varchar(25),
    nome_fantasia varchar(50),
    endereco varchar(100),
    telefone varchar(15)
);

create table Produto(
	id int primary key auto_increment,
    produto varchar(30),
    data_fab date,
    data_venc date,
    preco float,
    id_cat int,
    id_fornecedor int,
    foreign key (id_cat) references Categoria(id),
    foreign key (id_fornecedor) references Fornecedor(id)
);

create table Pedido(
	id int primary key auto_increment,
    data date,
    total_pedido float,
    id_cliente int,
    foreign key (id_cliente) references Cliente(id)
);

create table Itens_Pedido(
	id_ped int,
    id_prod int,
    quantidade int,
    subtotal float,
    primary key (id_ped, id_prod),
    foreign key (id_ped) references Pedido(id),
    foreign key (id_prod) references Produto(id)
);

